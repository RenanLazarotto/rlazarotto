<section class="prose prose-invert mb-16 prose-hr:border-dashed max-w-none">
    <h2>Aprendendo coisas novas</h2>

    <p>
        A reescrita por si só já é uma coisa grandiosa, mas junto dela vieram
        novos aprendizados.
    </p>
    <p>
        Primeiramente, me familiarizei com o TypeScript. No começo, era um bicho
        de sete cabeças que eu não queria enfrentar por puro medo. Hoje, o site
        todo é feito com base nele, e eu ainda estou naquele feeling de
        lua-de-mel. Já li bastante coisa a respeito das vantagens e desvantagens
        do TypeScript e minha visão sobre ele é 70/30: 70% a favor de
        implementar e usar TypeScript em absolutamente tudo e 30% que vê tudo
        com um certo ceticismo.
    </p>
    <p>
        A mudança de foco também me trouxe outro aprendizado: me aproximei mais
        do Markdown. Apesar de ser algo extremamente simples e bastante fácil de
        usar, aplicar isso num site de forma que eu consiga criar páginas ou
        publicações simplesmente escrevendo um arquivo, sem a necessidade de um
        banco de dados, por exemplo, envolveu muito mais do que eu conhecia até
        então - e eu aprendi muita coisa legal com isso no processo.
    </p>
    <p>
        Pra finalizar, novamente eu me vi usando o Tailwind CSS. É uma
        ferramenta poderosa que se vende como uma forma fácil de prática de
        fazer layouts pra quem não manja da refinada arte do CSS, mas há quem
        contra-argumente que CSS não é feito assim e menciona n motivos que
        corroboram a ideia. Um motivo em particular me chamou a atenção, que é o
        fato de que o código fica visualmente poluído com várias classes nos
        elementos, ao invés de umas poucas classes com todas as propriedades
        necessárias.
    </p>
    <p>
        Eu não vejo isso exatamente como um problema - é um projeto pessoal, que
        só eu vou manter, que em teoria só precisa fazer sentido pra mim.
        Enquanto na primeira versão eu fiz o caminho inverso - migrei do
        Tailwind para CSS puro, dessa vez mantive tudo no Tailwind e não vejo
        motivos pra ser de outra forma.
    </p>
</section>

